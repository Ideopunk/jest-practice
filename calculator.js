function add(num1, num2) {
    let answer = num1 + num2
    return answer
}

function subtract(num1, num2) {
    let answer = num1 - num2
    return answer
}

function multiply(num1, num2) {
    let answer = num1 * num2
    return answer
}

function divide(num1, num2) {
    let answer = num1 / num2
    return answer
}


module.exports = {add, subtract, multiply, divide}